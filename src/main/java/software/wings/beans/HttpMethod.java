package software.wings.beans;

/**
 * HttpMethod bean class.
 *
 * @author Rishi
 */
public enum HttpMethod {
  /**
   * Head http method.
   */
  HEAD, /**
         * Get http method.
         */
  GET, /**
        * Post http method.
        */
  POST, /**
         * Put http method.
         */
  PUT, /**
        * Delete http method.
        */
  DELETE;
}
